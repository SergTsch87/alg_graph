def main():
    
    # Adjacency list of graph:
    graph_dict = {
        "A": ["D", "B"],
        "B": ["A", "C", "E"],
        "C": ["B"],
        "D": ["A"],
        "E": ["B"]
    }

    visited_nodes_list = []
    queue_fifo_list = []
    current_pointer = ""

    # Initial
    current_pointer = "A"
    visited_nodes_list.append(current_pointer)

    for item in graph_dict[current_pointer][::-1]:
        queue_fifo_list.append(item)

    print(f'queue_fifo_list == {queue_fifo_list}')


if __name__ == '__main__':
    main()